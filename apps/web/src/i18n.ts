export function detectInitialLang(): Lang {
  const saved = localStorage.getItem("boltorezka_lang");
  if (saved === "ru" || saved === "en") {
    return saved;
  }

  const browser = navigator.language.toLowerCase();
  return browser.startsWith("ru") ? "ru" : "en";
}